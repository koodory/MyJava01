package onlinepractices;

public class operator01 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int result01 = a + b;
		
		System.out.println("a + b = " + result01);
		
		a = 5;
		b = 2;
		float result02 = a / b;
		
		System.out.println("a / b = " + result02);
		
		a = 5;
		b = 2;
		float result03 = (float)a / b;
		
		System.out.println("a / b = " + result03);
		
		a = 1200;
		byte c = (byte)a;
		System.out.println(c);
		
		int result = 10 + (-3) ;
		System.out.println(result);
		
				

	}

}
