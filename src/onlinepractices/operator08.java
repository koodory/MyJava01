package onlinepractices;

public class operator08 {

	public static void main(String[] args) {
		int var1 = 10;
	  var1 = var1 + 100;
	  System.out.println("var1 = var1 + 100 --> " + var1);
	  
	  int var2 = 10;
	  var2 += 100;
	  System.out.println("var2 = var2 + 100 --> " + var2);
	}	  
}
